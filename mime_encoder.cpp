string quoted_printable_decode(string mime, string charset){
    int mimeLenght = 0;
    int index = 0;
    int i = 0;
    char ascii_char;
    stringstream output;
    mimeLenght = mime.size();
    //cout << mimeLenght;
    for(i=0; i<mimeLenght; i++) {
        //cout << i;
        if(mime[i] == '_'){
            output << ' ';
        }else if(mime[i] == '='){
            if((mime[i+1] >= 48) && (mime[i+1] <= 57)){
                index = index + ((mime[i+1] - 48) * 16);
            }else if((mime[i+1] >= 65) && (mime[i+1] <= 70)){
                index = index + ((mime[i+1] - 55) * 16);
            }else if((mime[i+1] >= 97)&& (mime[i+1] <= 102)){
                index = index + ((mime[i+1] - 87) * 16);
            }else{
                //ERROR
                break;
            }


            if((mime[i+2] >= 48) && (mime[i+2] <= 57)){
                index = index + ((mime[i+2] - 48) * 1);
            }else if((mime[i+2] >= 65) && (mime[i+2] <= 70)){
                index = index + ((mime[i+2] - 55) * 1);
            }else if((mime[i+2] >= 97)&& (mime[i+2] <= 102)){
                index = index + ((mime[i+2] - 87) * 1);
            }else{
                //ERROR
                break;
            }

            if((charset == "UTF-8") || (charset == "utf-8")){
                if(index < 128){
                    ascii_char = index;
                    output << ascii_char;
                }else{
                    output << utf8_table(index);
                }
            }else{
                if(index < 128){
                    ascii_char = index;
                    output << ascii_char;
                }else{
                    output << charset_table(index, charset);
                }
            }

            index = 0;
            ascii_char = 0;
            i = i + 2;
        }else{
            output << mime[i];
        }
    }

    return output.str();
}

