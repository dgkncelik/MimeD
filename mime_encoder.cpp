//remove all the newline characters from input
string remove_newline(string input) {
    string output;
    for(int i=0; i<input.size(); i++) {
        if((input[i] != '\r') && (input[i] != '\n')) {
            output = output + input[i];
        }
    }
    return output;
}

//remove spaces between individuals mime
string remove_mime_space(string input) {
    int inputLength = input.size();
    int spaceBetweenMime = 0;
    int otherCharFind = 0;
    string output;
    for(int i=0; i<inputLength; i++) {
        if((input[i] == '?') && (input[i+1] == '=')) {
            output = output + input[i] + input[i+1];
            i=i+2;
            int j=i;

            if(j >= inputLength){
                break;
            }

            while((input[j] != '=') || (input[j+1] != '?')) {
                if(j >= inputLength) {
                    break;
                }

                if((input[j] == ' ') || (input[j] == '\t')) {
                    spaceBetweenMime++;
                } else {
                    otherCharFind = 1;
                }
                j++;
            }
        }

        if(otherCharFind != 1) {
            i = i + spaceBetweenMime;
        }
        output = output + input[i];

        otherCharFind = 0;
        spaceBetweenMime = 0;
    }
    return output;
}

